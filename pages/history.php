      <h1><b>Jasaan</b></h1>
      <img class="imgr borderedbox inspace-5" src="../images/hall1.jpg" alt="">
      <h5>Jasaan, officially the Municipality of Jasaan (Cebuano: Lungsod sa Jasaan; Tagalog: Bayan ng Jasaan), is a 2nd class municipality in the province of Misamis Oriental, Philippines. According to the 2015 census, it has a population of 54,478 people.</h5>
      <h5>It is approximately 28 kilometres (17 mi) east of the capital city of Cagayan de Oro. It borders on the municipalities of Villanueva, Claveria and Balingasag, while Macajalar Bay is to the west. Although its land is mostly rocky, it is able to maintain and support its lush vegetation and its main rivers, Cabulig, Dumagooc and Mandangisiao.</h5>
      
      
      <h1><b>Barangays<hr style="background-color:rgb(0, 0, 0)"></b></h1>
      <h5>Jasaan is politically subdivided into 15 barangays, of which 8 are coastal barangays (Aplaya, Solana, Luz Banzon, Kimaya, Lower Jasaan, Bobuntugan, Jampason and San Antonio) and 7 inland barangays (Upper Jasaan, Corrales, San Isidro, Natubo, Danao, San Nicolas, and Ignacio S. Cruz).</h5>

