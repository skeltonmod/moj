      <h1><b>History<hr style="background-color:rgb(0, 0, 0)"></b></h1>
      <img class="imgl borderedbox inspace-5" src="../images/map.png" alt="">
      <h5>In 1830, the mission of Jasaan was established separately from Cagayan de Oro, where its authority and evangelization reached as far as the towns of Sumilao, Linabo and Malitbog in the province of Bukidnon.</h5>
      <h5>The center of civilization of the new parish and its first Church was at "Daanglungsod," which is now the Aplaya, Jasaan, where an old kota (watchtower) still exists. This kota, however, has been moved a few meters from where it originally stood to allow for the construction of the national highway in the 1970s.</h5>
      <h5>Father Gregorio Parache, S.J. (432 local historical sources of Northern Mindanao by Father Francisco Demetrio, S.J.), was the parish priest of Jasaan at that time. The Jesuits later built the Nuestra Senora de Immaculada Concepcion Church in what is now the Immaculate Conception Church of Jasaan. The original facade of the church has been modified after a series of renovations. The original altar of the church has been moved backward to allow a larger area for the faithful inside the church building. The original sacristy has been moved to the side. The church is registered as a National Cultural Treasure by the National Commission for Culture and the Arts (NCCA).</h5>
      <h5>Jasaan is believed to have been already a municipality during the establishment of the Immaculate Conception Parish in 1840. The old church bells (four of them, excluding the one now at the San Agustin Cathedral at Cagayan de Oro) of the Immaculate Conception Church of Jasaan bore these inscriptions around its outer rim: "Para El Pueblo de Jasaan 1860" [more or less], which suggests that the Spanish government had recognized Jasaan as a town.</h5>

      <h5>With the coming of the Americans at the turn of the 20th century, the national government in 1903 downgraded the political status of Jasaan from that of a municipality to a barrio (a Spanish subdivision of a municipio) and made it a part of the Municipality of Balingasag. The Philippine Commission of 1903, Act No. 960, combined some municipalities in 1903 because the civil government had no control of these municipalities. They could not be defended by the Philippine constabulary or the scouts, nor could they be governed by the pro-American inhabitants!"[6] The Jasaanons called for the restoration of their municipio into a municipality. Eventually, on August 18, 1948, by virtue of Executive Order No. 165, issued by President Elpidio Quirino, Jasaan regained its municipal status. On November 10, 1948, Jasaan was inaugurated as a municipality, and a set of appointed municipal officials assumed office (from the website of Jasaan Local Government).</h5>
      <h5>The early 1970s saw the installation of electric power lines and a road widening program. Ubos had some remnant of Hispanic-style houses along its main street, but these were razed by fire in the early 1980s.</h5>
        
         
      <h1><b>Town center, poblacion<hr style="background-color:rgb(0, 0, 0)"></b></h1>
      <img class="imgr borderedbox inspace-5" src="../images/church1.jfif" alt="">
      <h5>The town center or poblacion of Jasaan is a picture of the Spanish Catholic concept of settlement model–"bajo de las campanas"–where people live around the vicinity of the church within earshot of the peal of the bells. There used to be a bell at the belfry of the Nuestra Senora de la Inmaculada Concepcion Church, where the peal could be heard as far as the hills of Natubo.</h5>
      <h5>This town center used to be one big poblacion, subdivided into nine puroks (hamlets): Purok Uno, Purok Dos, Purok Tres, Purok Cuatro, Purok Cinco, Purok Seis, Purok Siete, Purok Ocho, and Purok Nueve. In the 1960s, these villages adopted names, such as like Siga-Siga, Mauswagon and Mabuligon, etc.</h5>
      <h5>In the early 1970s, when martial law was in force, then-President Ferdinand Marcos (by a series of presidential decrees) made the barangay the basic political unit of Philippines. The Centro, as it used to known, was then split into Upper Jasaan and Lower Jasaan. Local residents would refer to these two as Ubos and Ibabaw, a vernacular indication of their geographical location in the slope: Ubos being Lower, and Ibabaw being Upper. Despite the partition of the Poblacion into two barangays, the old village identifications–the Purok–are still generally referred to in the present Jasaan.</h5>
      <h5>Jasaan town plaza is a good example of the separation of church and state property. A road runs through the middle of the plaza, making the demarcation line. The portion fronting the centuries-old church belongs to the Roman Catholic Church, and on portion on the other side of the road is owned by the local government of Jasaan.</h5>
      
